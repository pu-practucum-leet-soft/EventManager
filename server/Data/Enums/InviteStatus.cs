namespace EventManager.Data.Enums
{
    /// <summary>
    /// Представлява статуса на покана за участие в събитие.
    /// Използва се за проследяване на реакцията на поканения участник.
    /// </summary>
    public enum InviteStatus
    {
        /// <summary>
        /// Участникът е добавен към събитието и е със статус "Поканен",
        /// докато не приеме или откаже поканата.
        /// </summary>
        Invited = 0,

        /// <summary>
        /// Участникът е приел поканата и ще присъства на събитието.
        /// </summary>
        Accepted = 1,

        /// <summary>
        /// Участникът е отказал поканата и няма да присъства.
        /// </summary>
        Declined = 2
    }
}
